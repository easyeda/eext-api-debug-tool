/**
 * Build script to generate manifest of built-in projects from ext folder
 * Generates iframe/script/ext_manifest.js with all project files
 */

const fs = require('fs');
const path = require('path');

const EXT_DIR = path.join(__dirname, 'iframe', 'ext');
const OUTPUT_FILE = path.join(__dirname, 'iframe', 'script', 'ext_manifest.js');

/**
 * Recursively read all files in a directory
 */
function readDirRecursive(dir, baseDir = dir) {
	const files = [];
	const entries = fs.readdirSync(dir, { withFileTypes: true });

	for (const entry of entries) {
		const fullPath = path.join(dir, entry.name);
		if (entry.isDirectory()) {
			files.push(...readDirRecursive(fullPath, baseDir));
		} else if (entry.isFile()) {
			const relativePath = path.relative(baseDir, fullPath).replace(/\\/g, '/');
			files.push(relativePath);
		}
	}

	return files;
}

/**
 * Group files by project (top-level folder)
 */
function groupFilesByProject(files) {
	const projects = {};

	for (const file of files) {
		const parts = file.split('/');
		if (parts.length < 2) continue;

		const projectName = parts[0];
		const fileName = parts.slice(1).join('/');

		if (!projects[projectName]) {
			projects[projectName] = [];
		}

		projects[projectName].push(fileName);
	}

	return projects;
}

/**
 * Read file content with error handling
 */
function readFileContent(filePath) {
	try {
		return fs.readFileSync(filePath, 'utf-8');
	} catch (error) {
		console.warn(`Warning: Could not read ${filePath}: ${error.message}`);
		return '';
	}
}

/**
 * Read existing ext_manifest.js and extract per-project / per-file timestamps.
 * Preserves timestamps of already-known plugins so each build does not refresh
 * them. Only newly added projects/files get today's date.
 */
function readExistingTimestamps() {
	const map = new Map(); // projectName -> { createdAt, updatedAt, files: Map(fileName -> createdAt) }
	if (!fs.existsSync(OUTPUT_FILE)) return map;
	try {
		const content = fs.readFileSync(OUTPUT_FILE, 'utf-8');
		const startMarker = 'window.extManifest = ';
		const startIdx = content.indexOf(startMarker);
		if (startIdx === -1) return map;
		const arrStart = startIdx + startMarker.length;
		const endIdx = content.lastIndexOf('];');
		if (endIdx <= arrStart) return map;
		const parsed = JSON.parse(content.slice(arrStart, endIdx + 1));
		for (const proj of parsed) {
			if (!proj || typeof proj.projectName !== 'string') continue;
			const fileMap = new Map();
			if (Array.isArray(proj.files)) {
				for (const f of proj.files) {
					if (f && typeof f.fileName === 'string') fileMap.set(f.fileName, f.createdAt);
				}
			}
			map.set(proj.projectName, { createdAt: proj.createdAt, updatedAt: proj.updatedAt, files: fileMap });
		}
	} catch (e) {
		console.warn('无法解析已有的 ext_manifest.js，时间戳将重新生成。', e.message);
	}
	return map;
}

/**
 * Generate manifest
 */
function generateManifest() {
	console.log('Generating ext manifest...');

	if (!fs.existsSync(EXT_DIR)) {
		console.log('No ext directory found, creating empty manifest');
		const emptyManifest = 'window.extManifest = [];\n';
		fs.writeFileSync(OUTPUT_FILE, emptyManifest, 'utf-8');
		return;
	}

	// Read all files
	const allFiles = readDirRecursive(EXT_DIR);
	console.log(`Found ${allFiles.length} files in ext folder`);

	// Group by project
	const projectGroups = groupFilesByProject(allFiles);
	const projectNames = Object.keys(projectGroups);
	console.log(`Found ${projectNames.length} projects: ${projectNames.join(', ')}`);

	// Build manifest
	// 保留已存在项目/文件的时间戳，仅对新项目/新文件使用当天日期，
	// 避免每次构建都刷新已有插件的时间戳
	const existing = readExistingTimestamps();
	const today = new Date().toISOString().split('T')[0];
	const manifest = [];

	for (const projectName of projectNames) {
		const fileNames = projectGroups[projectName];
		const existingProj = existing.get(projectName);
		const files = [];

		for (const fileName of fileNames) {
			const fullPath = path.join(EXT_DIR, projectName, fileName);
			const content = readFileContent(fullPath);
			const existingFileCreatedAt = existingProj && existingProj.files.get(fileName);

			files.push({
				fileName,
				content,
				createdAt: existingFileCreatedAt || today,
			});
		}

		manifest.push({
			projectName,
			files,
			isBuiltIn: true,
			createdAt: (existingProj && existingProj.createdAt) || today,
			updatedAt: (existingProj && existingProj.updatedAt) || today,
		});

		console.log(`  - ${projectName}: ${files.length} files`);
	}

	// Write manifest
	const output = `/**
 * Built-in projects manifest
 * Auto-generated by build-ext-manifest.cjs
 * DO NOT EDIT MANUALLY
 */
window.extManifest = ${JSON.stringify(manifest, null, 2)};
`;

	fs.writeFileSync(OUTPUT_FILE, output, 'utf-8');
	console.log(`✓ Manifest written to ${OUTPUT_FILE}`);
	console.log(`✓ Total size: ${(output.length / 1024).toFixed(2)} KB`);
}

// Run
try {
	generateManifest();
} catch (error) {
	console.error('Error generating manifest:', error);
	process.exit(1);
}
